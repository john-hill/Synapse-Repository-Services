CREATE TABLE `ENTITY_REPLICATION` (
  `ID` bigint(20) NOT NULL,
  `CURRENT_VERSION` bigint(20) NOT NULL,
  `CREATED_BY` bigint(20) NOT NULL,
  `CREATED_ON` bigint(20) NOT NULL,
  `ETAG` char(36) NOT NULL,
  `NAME` varchar(256) CHARACTER SET latin1 COLLATE latin1_bin NOT NULL,
  `TYPE` enum('project','folder','file','table','link','entityview','dockerrepo') NOT NULL,
  `PARENT_ID` bigint(20) DEFAULT NULL,
  `BENEFACTOR_ID` bigint(20) NOT NULL,
  `PROJECT_ID` bigint(20) DEFAULT NULL,
  `MODIFIED_BY` bigint(20) NOT NULL,
  `MODIFIED_ON` bigint(20) NOT NULL,
  `FILE_ID` bigint(20) DEFAULT NULL,
  PRIMARY KEY (`ID`),
  KEY `CURRENT_VERSION` (`CURRENT_VERSION`),
  KEY `CREATED_BY` (`CREATED_BY`),
  KEY `CREATED_ON` (`CREATED_ON`),
  KEY `ETAG` (`ETAG`),
  KEY `NAME` (`NAME`),
  KEY `TYPE` (`TYPE`),
  KEY `PARENT_ID` (`PARENT_ID`),
  KEY `BENEFACTOR_ID` (`BENEFACTOR_ID`),
  KEY `PROJECT_ID` (`PROJECT_ID`),
  KEY `MODIFIED_BY` (`MODIFIED_BY`),
  KEY `MODIFIED_ON` (`MODIFIED_ON`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
